package com.ndb.auction.models.user;

public class TwoFAMethod {

    private String key;
    private Boolean value;

    public String getKey() {
        return key;
    }
    public void setKey(String key) {
        this.key = key;
    }
    public Boolean getValue() {
        return value;
    }
    public void setValue(Boolean value) {
        this.value = value;
    }

}
